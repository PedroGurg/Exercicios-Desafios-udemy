import sympy as sp
sp.init_printing()

Matriz_1 = sp.Matrix([[1,2],[4,5]])
#inversa
inv_1 = Matriz_1**(-1)
#Transposta
trans_1 = Matriz_1.T
#determinante
det_1 = Matriz_1.det()
print('matriz',Matriz_1)
print('inversa',inv_1)
print('transposta',trans_1)
print('determinante',det_1)

